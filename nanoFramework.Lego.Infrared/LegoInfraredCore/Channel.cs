        /// Represents channel 1.
        /// </summary>
        Channel1 = 0x0,

        /// <summary>
        /// Represents channel 2.
        /// </summary>
        Channel2 = 0x1,

        /// <summary>
        /// Represents channel 3.
        /// </summary>
        Channel3 = 0x2,

        /// <summary>
        /// Represents channel 4.
        /// </summary>
        Channel4 = 0x3
    }
}
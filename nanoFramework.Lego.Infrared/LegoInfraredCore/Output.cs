namespace Lego.Infrared
{
    /// <summary>
    /// Represents the different output pins.
    /// </summary>
    public enum Output
    {
        /// <summary>
        /// Represents the red pin C1.
        /// </summary>
        RedPinC1 = 0x0,

        /// <summary>
        /// Represents the red pin C2.
        /// </summary>
        RedPinC2 = 0x1,

        /// <summary>
        /// Represents the blue pin C1.
        /// </summary>
        BluePinC1 = 0x2,

        /// <summary>
        /// Represents the blue pin C2.
        /// </summary>
        BluePinC2 = 0x3
    }
}